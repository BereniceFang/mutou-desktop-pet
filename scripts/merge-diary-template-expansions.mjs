function mergeDeep(base, add) {
  if (Array.isArray(base) && Array.isArray(add)) {
    return [...base, ...add]
  }
  if (
    base &&
    add &&
    typeof base === 'object' &&
    typeof add === 'object' &&
    !Array.isArray(base)
  ) {
    const out = { ...base }
    for (const k of Object.keys(add)) {
      out[k] = mergeDeep(base[k], add[k])
    }
    return out
  }
  return base
}

